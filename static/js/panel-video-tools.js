/**
 * Drop Cat Go Studio — Video Tools panel.
 * Batch video transforms: reverse, mirror, flip, speed, upscale, sharpen.
 */
import { api, apiUpload, pollJob, stopJob } from './api.js';
import { toast, createDropZone, createPathInput, createProgressCard, createSlider, createSelect, createCheckbox, el, formatDuration } from './components.js';

let files = [];

export function init(panel) {
  panel.innerHTML = '';
  const cards = el('div', { class: 'step-cards' });
  panel.appendChild(cards);

  // ── File Queue ────────────────────────────────────────────────────────
  const queueCard = el('div', { class: 'step-card' });
  cards.appendChild(queueCard);
  queueCard.appendChild(el('h3', { text: 'File Queue' }));

  createDropZone(queueCard, {
    accept: 'video/*',
    label: 'Drop video files here',
    async onFiles(f) {
      try {
        const data = await apiUpload('/api/tools/upload', f);
        for (const file of data.files || []) files.push(file);
        renderFiles();
      } catch (e) { toast(e.message, 'error'); }
    },
  });

  // Accept paths from disk (other apps, external folders)
  createPathInput(queueCard, {
    placeholder: 'Or paste file/folder paths (comma-separated)...',
    async onPaths(paths) {
      try {
        const data = await api('/api/tools/add-paths', {
          method: 'POST',
          body: JSON.stringify({ paths }),
        });
        for (const f of data.files || []) {
          if (!f.error) files.push(f);
          else toast(`Skipped: ${f.name} — ${f.error}`, 'info');
        }
        renderFiles();
        toast(`Added ${data.files?.filter(f => !f.error).length || 0} file(s)`, 'success');
      } catch (e) { toast(e.message, 'error'); }
    },
  });

  // Session picker — show videos generated by other tabs
  const sessionBtn = el('button', { class: 'btn btn-sm', text: '+ From Session', style: 'margin-top:6px' });
  queueCard.appendChild(sessionBtn);
  const sessionPicker = el('div', { style: 'display:none; margin-top:8px' });
  queueCard.appendChild(sessionPicker);

  async function refreshSessionPicker() {
    try {
      const data = await api('/api/session/videos');
      sessionPicker.innerHTML = '';
      const vids = data.videos || [];
      if (!vids.length) { sessionPicker.textContent = 'No videos in session yet.'; return; }
      for (const v of vids) {
        const row = el('div', { class: 'file-item', style: 'cursor:pointer', onclick() {
          if (!files.find(f => f.path === v.path)) {
            files.push({ path: v.path, name: v.filename, source: v.source });
            renderFiles();
          }
          sessionPicker.style.display = 'none';
        }}, [
          el('span', { class: 'name', text: v.filename }),
          el('span', { class: 'meta', text: `from ${v.source}` }),
        ]);
        sessionPicker.appendChild(row);
      }
    } catch (e) { toast(e.message, 'error'); }
  }

  sessionBtn.addEventListener('click', async () => {
    const show = sessionPicker.style.display === 'none';
    sessionPicker.style.display = show ? '' : 'none';
    if (show) refreshSessionPicker();
  });

  window.addEventListener('session-updated', () => {
    if (sessionPicker.style.display !== 'none') refreshSessionPicker();
  });

  const fileList = el('div', { class: 'file-list' });
  queueCard.appendChild(fileList);

  function renderFiles() {
    fileList.innerHTML = '';
    files.forEach((f, i) => {
      fileList.appendChild(el('div', { class: 'file-item' }, [
        el('span', { class: 'name', text: f.name || `File ${i + 1}` }),
        el('span', { class: 'meta', text: `${formatDuration(f.duration)}  ${f.width || '?'}x${f.height || '?'}  ${f.has_audio ? 'Audio' : 'No audio'}` }),
        el('button', { class: 'remove', text: '✕', onclick() { files.splice(i, 1); renderFiles(); } }),
      ]));
    });
  }

  // ── Effects ───────────────────────────────────────────────────────────
  const fxCard = el('div', { class: 'step-card' });
  cards.appendChild(fxCard);
  fxCard.appendChild(el('h3', { text: 'Effects' }));

  const reverseVid = createCheckbox(fxCard, { label: 'Reverse Video', checked: true });
  const mirror = createCheckbox(fxCard, { label: 'Mirror (H-Flip)' });
  const vflip = createCheckbox(fxCard, { label: 'V-Flip' });
  const speed = createSlider(fxCard, { label: 'Playback Speed', min: 0.25, max: 4, step: 0.25, value: 1, unit: 'x' });

  // ── Audio ─────────────────────────────────────────────────────────────
  const audioCard = el('div', { class: 'step-card' });
  cards.appendChild(audioCard);
  audioCard.appendChild(el('h3', { text: 'Audio' }));

  const keepAudio = createCheckbox(audioCard, { label: 'Keep Audio', checked: true });
  const reverseAud = createCheckbox(audioCard, { label: 'Reverse Audio', checked: true });
  const muteAudio = createCheckbox(audioCard, { label: 'Mute' });
  const volume = createSlider(audioCard, { label: 'Volume', min: 0, max: 300, step: 5, value: 100, unit: '%' });

  // ── Upscale & Output ──────────────────────────────────────────────────
  const outCard = el('div', { class: 'step-card' });
  cards.appendChild(outCard);
  outCard.appendChild(el('h3', { text: 'Upscale & Output' }));

  const upscale = createCheckbox(outCard, { label: 'Upscale Output' });
  const upscaleMode = createSelect(outCard, { label: 'Scale', options: ['1.5x', '2x', '3x', '4x'], value: '2x' });
  const upscaleMethod = createSelect(outCard, { label: 'Method', options: ['lanczos', 'bicubic', 'bilinear', 'spline'], value: 'lanczos' });
  const sharpen = createCheckbox(outCard, { label: 'Sharpen' });
  const sharpenStr = createSlider(outCard, { label: 'Sharpen Strength', min: 0.5, max: 3, step: 0.1, value: 1 });
  const outFormat = createSelect(outCard, { label: 'Format', options: ['mp4', 'mkv', 'mov', 'webm'], value: 'mp4' });
  const crf = createSlider(outCard, { label: 'Quality (CRF)', min: 0, max: 51, step: 1, value: 18, unit: '' });

  // ── Process ───────────────────────────────────────────────────────────
  const processBtn = el('button', { class: 'btn btn-primary', text: 'START BATCH', style: 'margin-top:16px; font-size:1.1rem; padding:12px 32px' });
  cards.appendChild(processBtn);

  const progress = createProgressCard(panel);

  processBtn.addEventListener('click', async () => {
    if (!files.length) { toast('Add video files first', 'error'); return; }
    processBtn.disabled = true;
    progress.show();

    try {
      const data = await api('/api/tools/process', {
        method: 'POST',
        body: JSON.stringify({
          files: files.map(f => ({ path: f.path })),
          settings: {
            reverse_vid: reverseVid.checked,
            mirror: mirror.checked,
            vflip: vflip.checked,
            speed: speed.value,
            keep_audio: keepAudio.checked,
            reverse_aud: reverseAud.checked,
            mute_audio: muteAudio.checked,
            volume: volume.value,
            upscale: upscale.checked,
            upscale_mode: upscaleMode.value,
            upscale_method: upscaleMethod.value,
            sharpen: sharpen.checked,
            sharpen_str: sharpenStr.value,
            out_format: outFormat.value,
            crf: crf.value,
          },
        }),
      });

      progress.onCancel(async () => { await stopJob(data.job_id); processBtn.disabled = false; });
      pollJob(data.job_id,
        job => progress.update(job.progress, job.message),
        job => { progress.hide(); processBtn.disabled = false; toast(`Processed ${job.meta?.processed || 0} files`, 'success'); },
        err => { progress.hide(); processBtn.disabled = false; toast(err, 'error'); },
      );
    } catch (e) { progress.hide(); processBtn.disabled = false; toast(e.message, 'error'); }
  });
}
